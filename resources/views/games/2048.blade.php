                <!-- Instructions -->
                <div class="settings-card">
                    <h4 style="margin-bottom: 12px;">üìã Instructions</h4>
                    <ul style="color: #6b7280; font-size: 14px; line-height: 1.8; padding-left: 20px;">
                        <li>Utilisez les <strong>fl√®ches du clavier</strong> (‚Üë ‚Üì ‚Üê ‚Üí) pour d√©placer les tuiles</li>
                        <li>Quand deux tuiles avec le <strong>m√™me nombre</strong> se touchent, elles <strong>fusionnent</strong></li>
                        <li>Chaque fusion double la valeur de la tuile</li>
                        <li>Atteignez la tuile <strong>2048</strong> pour gagner !</li>
                        <li>Le jeu se termine quand aucun mouvement n'est possible</li>
                        <li>Votre meilleur score est sauvegard√© automatiquement</li>
